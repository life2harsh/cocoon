export type TemplateType = keyof typeof PROMPTS;

export function getPromptForToday(templateType: string): string {
  const pool = PROMPTS[templateType] || PROMPTS.free_write;
  const today = new Date();
  const start = new Date(today.getFullYear(), 0, 0);
  const diff = today.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const yearSeed = today.getFullYear() * 7;
  const index = (dayOfYear + yearSeed) % pool.length;
  return pool[index];
}
